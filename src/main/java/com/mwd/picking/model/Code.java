package com.mwd.picking.model;

public class Code {
	private String codeDiv;
	public String getCodeDiv() {
		return codeDiv;
	}
	public void setCodeDiv(String codeDiv) {
		this.codeDiv = codeDiv;
	}
	public String getEtcCode() {
		return etcCode;
	}
	public void setEtcCode(String etcCode) {
		this.etcCode = etcCode;
	}
	public String getEtcName() {
		return etcName;
	}
	public void setEtcName(String etcName) {
		this.etcName = etcName;
	}
	private String etcCode;
	private String etcName;
}
